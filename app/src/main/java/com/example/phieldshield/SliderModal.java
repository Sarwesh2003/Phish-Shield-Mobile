package com.example.phieldshield;

public class SliderModal {

        // image url is used to
        // store the url of image
        private int imgUrl;

        // Constructor method.
        public SliderModal(int imgUrl) {
            this.imgUrl = imgUrl;
        }

        // Getter method
        public int getImgUrl() {
            return imgUrl;
        }

        // Setter method
        public void setImgUrl(int imgUrl) {
            this.imgUrl = imgUrl;
        }

}
